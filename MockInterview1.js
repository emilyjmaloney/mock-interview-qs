// 1.	Write a program that sums all the numbers divisible by 3 or 5 below 1000. For example: All the numbers divisible by 3 or 5 below 10 are: 3,5,6,9 and their sum is 23.

function sumDivisibleNums(){
        for (let i=1; i <= 1000; i++) {
            if (i % 3 == 0);
            else if (i % 5 == 0);
        }
}
